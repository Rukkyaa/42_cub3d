#include "cub3d.h"

void	vec_print(t_vector *vector, char *name)
{
	printf("-----------------------------------------------\n");
	printf("| %s : x : %f | y : %f   |\n", name, vector->x, vector->y);
	printf("-----------------------------------------------\n");
}

void	vec3_print(t_vector3d vector, char *name)
{
	printf("----------------------------------------------------------\n");
	printf("| %s : x : %f | y : %f| z : %f    |\n", name, vector.x, vector.y, vector.z);
	printf("----------------------------------------------------------\n");
}
